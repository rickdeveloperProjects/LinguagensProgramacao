valorA = int(input("Digite o valor de A: "))
valorB = int(input("Digite o valor de B: "))
valorC = int(input("Digite o valor de C: "))

print ("\nvalorA = {}, valorB = {}, valorC = {} ".format(valorA,valorB,valorC))

if valorA > valorB + valorC:
    print ('\nOs valores nao formam um triangulo pois A > B + C')

else: 
    print ('Os valores formam um triangulo')









